<script lang="ts">
import debounce from "debounce";
import { Component, Prop } from "vue-property-decorator";

import { widgetValPrimitive } from "@/data models/widgetModels";
import Widget from "@/components/Widget";
import { logMessage } from "@/helpers/debugHelpers";
import WidgetEnabled from "@/mixins/WidgetEnabled";
import WidgetSharedData from "@/mixins/WidgetSharedData";

import { CODON_DICTIONARY } from "./CodonDictionary";

@Component({ mixins: [WidgetEnabled, WidgetSharedData] })
export default class WidgetCodonWheel extends Widget {
	@Prop({ default: "" })
	codonCode: string;

	@Prop({ default: "" })
	aminoAcid: string;

	@Prop({ default: "" })
	codonCode_unlocked: string;

	mounted() {
		const {
			$props: { sharedDataReadKey, sharedDataWriteKey },
			propSharedDataOverride,
			updateWidget,
			updateSharedDataVal,
		} = this;

		if (sharedDataReadKey) {
			const sharedValueOveride = propSharedDataOverride(
				"codonCode",
				sharedDataReadKey
			);
			updateWidget("codonCode", sharedValueOveride);

			if (sharedDataWriteKey) {
				updateSharedDataVal(sharedDataWriteKey, sharedValueOveride);
			}
		}
	}

	get codonCodeStore(): string {
		return this.$props.sharedDataReadKey
			? (this.propSharedDataOverride(
					"codonCode",
					this.$props.sharedDataReadKey
			  ) as string)
			: (this.propStoreOverride("codonCode") as string);
	}

	get codonCodeUnlockedStore(): string {
		return this.propStoreOverride("codonCode_unlocked") as string;
	}

	isCodonCodeIndex(
		index: number,
		code: "A" | "G" | "U" | "C" | "none"
	): boolean {
		if (this.codonCodeStore[index] !== undefined) {
			return this.codonCodeStore[index] === code;
		}

		return code === "none";
	}

	isCodonCodeComplete(): boolean {
		return this.codonCodeStore.length >= 3;
	}

	hasCodonSelected(index: number) {
		return this.codonCodeStore[index] !== undefined;
	}

	hasAminoAcidSelected(amino: string) {
		return this.getAminoAcid === amino;
	}

	get getAminoAcid(): string {
		// set the aminoAcid value based on its lookup value
		const { codonCodeStore: codon } = this;

		const aminoAcid = CODON_DICTIONARY[codon] || codon;

		if (codon.length !== 3) {
			// if the string does not match the form of a codon,
			if (this.propStoreOverride("aminoAcid") !== "") {
				// update the value with an empty string
				this.updateWidget("aminoAcid", "");
			}
			return ""; // exit the function by returning an empty string
		}

		if (this.propStoreOverride("aminoAcid") !== aminoAcid) {
			this.updateWidget("aminoAcid", aminoAcid);
		}

		return aminoAcid;
	}

	codonClicked(event: Event) {
		const buttonID = (event!.target! as Element).parentElement!.id.replace(
			"cw-",
			""
		);
		if (
			this.codonCodeUnlockedStore != "" &&
			!this.codonCodeUnlockedStore.startsWith(buttonID)
		) {
			return;
		}

		this.updateWidget("codonCode", buttonID);
		this.updateWidget("aminoAcid", ""); //reset the amino acid in case they are clicking in the same group.
		if (this.$props.sharedDataWriteKey) {
			this.updateSharedDataVal(this.$props.sharedDataWriteKey, buttonID);
		}
	}
}
</script>

